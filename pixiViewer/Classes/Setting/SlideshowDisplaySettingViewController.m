- (void)viewWillDisappear:(BOOL)animated {
	[super viewWillDisappear:animated];
	
	id obj;
	if (index == 0) {
		obj = @"AspectFit";
	} else {
		obj = @"AspectFill";
	}
	[[NSUserDefaults standardUserDefaults] setObject:obj forKey:@"SlideshowDisplay"];
	[[NSUserDefaults standardUserDefaults] synchronize];
}

