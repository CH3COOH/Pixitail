- (void)viewDidLoad {
    [super viewDidLoad];

	if ([[[NSUserDefaults standardUserDefaults] objectForKey:@"SlideshowDisplay"] isEqualToString:@"AspectFill"]) {
		index = 1;
	} else {
		index = 0;
	}

